#!/usr/bin/php
<?php
$points = array(
	0, 0,
	100, 0,
	100, 100,
	0, 100,
	0, 0,
	-100, 0,
	-100, -100,
	0, -100,
	0, 0,
	100, 0,
	100, -100,
	200, -100,
	200, 0,
	100, 0,
	100, 100,
	200, 100,
	200, 200,
	100, 200,
	100, 100,
	0, 100,
	0, 200,
	-100, 200,
	-100, 100,
	0, 100,
	-100, 100,
	-200, 100,
	-200, 0,
	-100, 0,
	-100, 100,
	-100, -100,
	-200, -100,
	-200, -200,
	-100, -200,
	-100, -100,
	0, -100,
	0, -200,
	100, -200,
	100, -100,
	0, -100,
	-100, -100,
	-100, 100,
	0, 100
);
$ang = 3.14159265358979 / 4;
$sine = sin($ang);
$cosine = cos($ang);

for($n = 0; $n < count($points); $n += 2){
	$newx = round($cosine * $points[$n] - $sine * $points[$n + 1]);
	$newy = round($sine * $points[$n] + $cosine * $points[$n + 1]);
	echo $newx . " " . $newy . "\n";
}
